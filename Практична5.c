#include <stdio.h>

#define MOD 12345

int main() {
    int n;
    printf("Введіть довжину послідовностей n (1 < n < 10000): ");
    scanf("%d", &n);

    if (n <= 1 || n >= 10000) {
        printf("Некоректне число. Введіть число в межах 1 < n < 10000.\n");
        return 1;
    }

    // Масив для зберігання кількості допустимих послідовностей довжиною i
    int dp[n+1];

    // Початкові значення
    dp[0] = 1;  // порожня послідовність
    dp[1] = 2;  // 0 або 1
    dp[2] = 4;  // 00, 01, 10, 11

    for (int i = 3; i <= n; i++) {
        dp[i] = (dp[i-1] + dp[i-2] + dp[i-3]) % MOD;
    }

    printf("Кількість допустимих послідовностей довжиною %d: %d\n", n, dp[n]);
    return 0;
}
